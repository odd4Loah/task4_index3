package com.lihoo.ssm.model;

public class IndexLearn {
    private Long id;

    private Integer learnId;

    private String learnImg;

    private String learnKeyword;

    private String learnInfo;

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public Integer getLearnId() {
        return learnId;
    }

    public void setLearnId(Integer learnId) {
        this.learnId = learnId;
    }

    public String getLearnImg() {
        return learnImg;
    }

    public void setLearnImg(String learnImg) {
        this.learnImg = learnImg;
    }

    public String getLearnKeyword() {
        return learnKeyword;
    }

    public void setLearnKeyword(String learnKeyword) {
        this.learnKeyword = learnKeyword;
    }

    public String getLearnInfo() {
        return learnInfo;
    }

    public void setLearnInfo(String learnInfo) {
        this.learnInfo = learnInfo;
    }
}